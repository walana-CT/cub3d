#include "libft.h"

int	ft_is_in(const char c, const char *s)
{
	size_t	i;

	if (!s || !c)
		return (-1);
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (1);
		i++;
	}
	return (-1);
}
